package nu.xell.util.functional;

public class Tuple5<A, B, C, D, E> {

	public final A first;
	public final B second;
	public final C third;
	public final D fourth;
	public final E fifth;
	
	public Tuple5(A a, B b, C c, D d, E e) {
		first = a;
		second = b;
		third = c;
		fourth = d;
		fifth = e;
	}
}
